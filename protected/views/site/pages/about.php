<h2>Status Update, January 2011</h2>
<h3>Written by: Paul Lowndes</h3>

<p>
    Aloha, and thank you for your interest in possibly joining or helping to start a HackerSpace on Oahu.
</p>

<p>
    I found out about HackerSpace from an existing member in the ##proggit chatroom on FreeNode.
    As you might be aware, ##proggit is a group of programmers with a strong satirical sense who hail primarily from the great reddit.
    After learning about what a HackerSpace does, and after seeing that there was no such space on Oahu, I was compelled to at least start a page and see what kind of interest existed.
    The only advertising for this landing page was a listing for a planned space on the hackerspace site.
</p>

<p>
    Surprisingly, there was more interest than I had anticipated.  So, if you are here, perhaps you have received an email regarding the status of HackerSpace Oahu, or have linked from <a href="http://www.techhui.com/">TechHui</a>.
</p>

<p>
    Maybe the best location would be in Honolulu as this is where most people who contacted me are located.  The question becomes, where can we get a space?  Who can subsidize it, if anyone?
    Maybe somebody with a decent wallet and some aloha spirit might be able to chip in.
    Or, maybe there is a state-funded or goverment-funded program that can assist us?
    I am open to options and suggestions.
    I used to spend a great deal of time tinkering with electronics in my youth, and the possibility of having a HackerSpace here on Oahu is an exciting one.
</p>

